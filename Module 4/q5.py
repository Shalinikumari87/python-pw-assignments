"""
question: Explain the concept of type casting in Python with examples.
Answer:
        Type casting is a method in which we can change data type. It is also known as type 
        conversion.
        
"""

# converting float into integer.
x = 3.5
print(int(x))   #3

# converting integer into float.
a = 3
print(float(a))  #3.0

# converting string to integer
n = '2' 
print(int(n))   #2

# converting integer to string
m = 3
string_variable = str(m)   #2 as a string
print(type(string_variable))
print(type(m))

# converting into booleans.
p = 1 
print(bool(p))  #true



